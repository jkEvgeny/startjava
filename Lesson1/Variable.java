public class Variable {
    public static void main(String[] args) {
        byte b = 1;
        short s = 5;
        int i = 8;
        long l = 1;
        float f = 1;
        double d = 1.6;
        char c = 'i';
        boolean bool = false;

        System.out.println("Values: " + b + " " +
                s + " " +
                i + " " +
                l + " " +
                f + " " +
                d + " " +
                c + " " +
                bool);
    }
}