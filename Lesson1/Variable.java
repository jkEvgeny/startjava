public class Variable {
    public static void main(String[] args) {
        byte byteValue = 1;
        short shortValue = 5;
        int intValue = 8;
        long longValue = 1;
        float floatValue = 1;
        double doubleValue = 1.6;
        char charValue = 'i';
        boolean booleanValue = false;

        System.out.println("Values: " + byteValue + " " +
                shortValue + " " +
                intValue + " " +
                longValue + " " +
                floatValue + " " +
                doubleValue + " " +
                charValue + " " +
                booleanValue);
    }
}